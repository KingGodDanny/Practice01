package com.javaex.practice;

public class Ex04 {

	public static void main(String[] args) {
		
		int x,y;                         //항상 변수와 속성,속성값이 끝날땐 ; 을 붙여줘야한다.
		
		x = 10;
		y = 20;
		
		int sum = x + y; 				// 앞에 자료형을 알맞게 넣어줘야한다.
		
		System.out.println("합은" + sum);   //합은 이라는 문자열을 넣을땐 ""으로 정확하게 넣어줘야한다
											
	}

}
